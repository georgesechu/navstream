/**
 * Bar chart accent based on alert count relative to max.
 */
function alertCountAccent(
  count: number,
  maxCount: number
): "red" | "amber" | "green" {
  if (count >= maxCount * 0.6) return "red";
  if (count >= maxCount * 0.3) return "amber";
  return "green";
}

/**
 * Format minutes into a human-readable string.
 */
function formatMinutes(minutes: number): string {
  if (minutes < 60) return `${minutes} min`;
  const hours = Math.floor(minutes / 60);
  const mins = minutes % 60;
  if (mins === 0) return `${hours}h`;
  return `${hours}h ${mins}m`;
}

/**
 * Generate a relative time string (e.g. "25 min ago", "2 hours ago").
 */
function relativeTime(dateStr: string): string {
  const date = new Date(dateStr);
  const now = new Date();
  const diffMs = now.getTime() - date.getTime();
  const diffMin = Math.round(diffMs / (1000 * 60));

  if (diffMin < 1) return "just now";
  if (diffMin < 60) return `${diffMin} min ago`;
  const diffHours = Math.round(diffMin / 60);
  if (diffHours < 24) return `${diffHours} hour${diffHours === 1 ? "" : "s"} ago`;
  const diffDays = Math.round(diffHours / 24);
  return `${diffDays} day${diffDays === 1 ? "" : "s"} ago`;
}

export interface AnalyticsKpi {
  systemUptime: string;
  activeAlerts: string;
  tasksCompleted: string;
  avgResponseTime: string;
}

export interface AlertBySiteBar {
  label: string;
  value: number;
  accent: "red" | "amber" | "green";
}

export interface RecentEvent {
  id: string;
  event: string;
  site: string;
  time: string;
  severity: "critical" | "warning" | "info" | "resolved";
}

export interface AnalyticsData {
  kpis: AnalyticsKpi;
  alertsBySite: AlertBySiteBar[];
  recentEvents: RecentEvent[];
  costSavings: { totalSaved: number; totalRemoteResolutions: number } | null;
  isLoading: boolean;
  error: Error | null;
}

