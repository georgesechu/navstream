/**
 * Fetches alerts, work orders, and sites, then computes analytics KPIs
 * using domain logic functions. Falls back to hardcoded data if APIs fail.
 */
export function useAnalytics(): AnalyticsData {
  const {
    data: alertsData,
    isLoading: alertsLoading,
    error: alertsError,
  } = useFetch<Alert[]>("/api/alerts");

  const {
    data: workOrdersData,
    isLoading: workOrdersLoading,
    error: workOrdersError,
  } = useFetch<WorkOrder[]>("/api/work-orders");

  const {
    data: sitesData,
    isLoading: sitesLoading,
    error: sitesError,
  } = useFetch<ApiSiteMinimal[]>("/api/sites");

  const isLoading = alertsLoading || workOrdersLoading || sitesLoading;
  const error = alertsError || workOrdersError || sitesError;

  const hasData = alertsData !== null && workOrdersData !== null;

  const kpis = useMemo((): AnalyticsKpi => {
    if (!hasData) return fallbackKpis;

    const alerts = alertsData!;
    const workOrders = workOrdersData!;

    const activeCount = alerts.filter((a) => a.status === "active").length;
    const completedCount = workOrders.filter(
      (wo) => wo.status === "completed"
    ).length;
    const resolutionTime = avgResolutionTime(alerts);

    // Compute average uptime from sites data or fallback
    const uptimeStr = sitesData && sitesData.length > 0
      ? (() => {
          const sitesFull = sitesData as Array<ApiSiteMinimal & { uptime?: number }>;
          const uptimes = sitesFull
            .map((s) => s.uptime)
            .filter((u): u is number => u !== undefined && u !== null);
          if (uptimes.length === 0) return fallbackKpis.systemUptime;
          const avg = uptimes.reduce((sum, u) => sum + u, 0) / uptimes.length;
          return `${avg.toFixed(1)}%`;
        })()
      : fallbackKpis.systemUptime;

    return {
      systemUptime: uptimeStr,
      activeAlerts: String(activeCount),
      tasksCompleted: String(completedCount),
      avgResponseTime: resolutionTime !== null
        ? formatMinutes(resolutionTime)
        : fallbackKpis.avgResponseTime,
    };
  }, [hasData, alertsData, workOrdersData, sitesData]);

  const alertsBySiteData = useMemo((): AlertBySiteBar[] => {
    if (!alertsData || !sitesData) return fallbackAlertsBySite;

    const siteNames: Record<string, string> = {};
    for (const site of sitesData) {
      siteNames[site.id] = site.name;
    }

    const bySite = alertsBySite(alertsData, siteNames);
    if (bySite.length === 0) return fallbackAlertsBySite;

    const maxCount = bySite[0]?.count ?? 1;

    return bySite.map((entry) => ({
      label: entry.siteName,
      value: entry.count,
      accent: alertCountAccent(entry.count, maxCount),
    }));
  }, [alertsData, sitesData]);

  const recentEvents = useMemo((): RecentEvent[] => {
    if (!alertsData || alertsData.length === 0) return fallbackRecentEvents;

    // Use the most recent alerts as recent events
    return alertsData
      .slice(0, 10)
      .map((alert) => ({
        id: alert.id as string,
        event: alert.title,
        site: (() => {
          if (!sitesData) return alert.siteId as string;
          const site = sitesData.find((s) => s.id === (alert.siteId as string));
          return site?.name ?? (alert.siteId as string);
        })(),
        time: relativeTime(alert.createdAt as string),
        severity: alert.status === "resolved"
          ? ("resolved" as const)
          : alert.severity,
      }));
  }, [alertsData, sitesData]);

  const costSavings = useMemo(() => {
    if (!alertsData) return null;
    const report = computeCostSavings(alertsData, 12000, "This Quarter");
    return {
      totalSaved: report.totalSaved,
      totalRemoteResolutions: report.totalRemoteResolutions,
    };
  }, [alertsData]);

  return {
    kpis,
    alertsBySite: alertsBySiteData,
    recentEvents,
    costSavings,
    isLoading,
    error,
  };
}
